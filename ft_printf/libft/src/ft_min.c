#include "../include/libft.h"

int		ft_min(int *tab, size_t n)
{
	size_t	i;
	int		min;

	i = 0;
	if (!tab || n == 0)
		return (0);
	min = tab[0];
	while (i < n)
	{
		if (tab[i] < min)
			min = tab[i];
		i++;
	}
	return (min);
}
